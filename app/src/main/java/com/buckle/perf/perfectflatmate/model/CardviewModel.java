public class CardviewModel {
    private int profileCardImage;
    private String profileName;
    private String profileIntro;
    private String matchingPercent;

    public int getProfileCardImage() {
        return profileCardImage;
    }

    public void setProfileCardImage(int profileCardImage) {
        this.profileCardImage = profileCardImage;
    }

    public String getProfileName() {
        return profileName;
    }

    public void setProfileName(String profileName) {
        this.profileName = profileName;
    }

    public String getProfileIntro() {
        return profileIntro;
    }

    public void setProfileIntro(String profileIntro) {
        this.profileIntro = profileIntro;
    }

    public String getMatchingPercent() {
        return matchingPercent;
    }

    public void setMatchingPercent(String matchingPercent) {
        this.matchingPercent = matchingPercent;
    }
}
